# programa converte ângulos em radianos para graus

print('OLÁ USUÁRIO(A) ! AGORA VAMOS TRABALHAR ÂNGULOS EM RADIANOS PARA GRAUS !!')
print('SIGA AS INSTRUÇÕES A SEGUIR')

RAD: float = float(input('DIGITE O VALOR DO ÂNGULO EM RADIANOS : '))

GRAUS: float = RAD * 180 / 3.1418

print('ÂNGULO EM RADIANO    : {:.2f} RADIANOS '.format(RAD))
print('ÂNGULO EM GRAUS      : {:.2f} GRAUS '.format(GRAUS))
