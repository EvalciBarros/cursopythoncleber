# programa converte distância em kilômetros para milhas

print('OLÁ USUÁRIO(A) ! AGORA VAMOS TRABALHAR AS DISTÂNCIAS EM QUILÔMETROS E MILHAS!!')
print('SIGA AS INSTRUÇÕES A SEGUIR')

KM: float = float(input('DIGITE O VALOR DA DISTÂNCIA EM KM : '))

MILHAS: float = KM / 1.61

print('DISTÂNCIA EM KM : {:.2f} KM '.format(KM))
print('DISTÂNCIA EM MILHAS  : {:.2f} MILHAS '.format(MILHAS))
