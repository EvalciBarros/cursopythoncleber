# programa converte massa em quilogramas para libras

print('OLÁ USUÁRIO(A) ! AGORA VAMOS TRABALHAR MASSAS EM QUILOGRAMAS PARA LIBRAS !!')
print('SIGA AS INSTRUÇÕES A SEGUIR')

QUILOS: float = float(input('DIGITE O VALOR DA MASSA EM QUILOS : '))

LIBRAS: float = QUILOS / 0.45

print('MASSA EM QUILOS : {:.2f} Kg '.format(QUILOS))
print('MASSA EM LIBRAS : {:.2f} Lb '.format(LIBRAS))